package module3;

public class ForDemo {

	public static void main(String[] args) {
		//display name 1 to 10
		for(int i=0;i<10;i++)
		{
			System.out.println(i+" Ankita");
		}
		System.out.println("----------------------");
		//while loop 1 to 10
		int a=1;
		while(a<=10)
		{
			System.out.println(a);
			a++;
		}
		System.out.println("----------------------");
		//do while loop 1 to 10
		int b=1;
		do
		{
			System.out.println(b);
			b++;
		}while(b<=10);
	}

}
